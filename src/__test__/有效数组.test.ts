import { sign } from 'crypto'
import isValidSudoku from '../leetcode/数独'

test('有效数独', () => {
	expect(
		isValidSudoku([
			['5', '3', '.', '.', '7', '.', '.', '.', '.'],
			['6', '.', '.', '1', '9', '5', '.', '.', '.'],
			['.', '9', '8', '.', '.', '.', '.', '6', '.'],
			['8', '.', '.', '.', '6', '.', '.', '.', '3'],
			['4', '.', '.', '8', '.', '3', '.', '.', '1'],
			['7', '.', '.', '.', '2', '.', '.', '.', '6'],
			['.', '6', '.', '.', '.', '.', '2', '8', '.'],
			['.', '.', '.', '4', '1', '9', '.', '.', '5'],
			['.', '.', '.', '.', '8', '.', '.', '7', '9']
		])
	).toBe(true)
})

test('无效数独', () => {
	expect(
		isValidSudoku([
			['8', '3', '.', '.', '7', '.', '.', '.', '.'],
			['6', '.', '.', '1', '9', '5', '.', '.', '.'],
			['.', '9', '8', '.', '.', '.', '.', '6', '.'],
			['8', '.', '.', '.', '6', '.', '.', '.', '3'],
			['4', '.', '.', '8', '.', '3', '.', '.', '1'],
			['7', '.', '.', '.', '2', '.', '.', '.', '6'],
			['.', '6', '.', '.', '.', '.', '2', '8', '.'],
			['.', '.', '.', '4', '1', '9', '.', '.', '5'],
			['.', '.', '.', '.', '8', '.', '.', '7', '9']
		])
	).toBe(false)
})
